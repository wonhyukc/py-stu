#!/usr/bin/env bash
# harness-check.sh
# 목적: 파이썬 코드 정합성(포맷, 린팅, 타입, 테스트)을 검증하는 CI/CD 게이트웨이 스크립트.

set -e # 에러 발생 시 스크립트 즉시 중단

echo "🚀 [Harness Check] 검증 루프를 시작합니다..."

# 환경에 가상환경이 있을 경우 활성화
if [ -d ".venv" ]; then
    source .venv/bin/activate
fi

# =======================================================
# 1. 포매팅 검사 (Black)
# =======================================================
echo "--> 1/4. Black 포매팅 검증 중..."
# --check 플래그를 주어, 포맷이 맞지 않으면 실패(Exit 1)하도록 합니다.
black --check .
echo "✅ Black 포매팅 통과!"

# =======================================================
# 2. 린터 검사 (Flake8)
# =======================================================
echo "--> 2/4. Flake8 린팅 검사 중..."
# E501(줄 길이) 등 일부 룰은 프로젝트에 맞게 Ignore 가능합니다.
flake8 . --count --select=E9,F63,F7,F82 --show-source --statistics
flake8 . --count --max-complexity=10 --max-line-length=127 --statistics
echo "✅ Flake8 린팅 통과!"

# =======================================================
# 3. 타입 힌트 검사 (Mypy) - 기본 단계
# =======================================================
echo "--> 3/4. Mypy 정적 타입 검사 중..."
# 모든 기존 코드 변경이 아니면 기본 모드로 구동
mypy . --ignore-missing-imports
echo "✅ Mypy 타임 검사 통과!"

# =======================================================
# 4. 단위 테스트 (Pytest)
# =======================================================
echo "--> 4/4. Pytest 실행 중..."
pytest
echo "✅ 유닛 테스트 통과!"

echo "🎉 [Harness Check] 모든 검증을 성공적으로 마쳤습니다!"
exit 0
